const now = new Date().toISOString();

function placeholderPhoto(id: string, x: number, y: number, width: number, height: number, zIndex: number, label = "Photo"): LabPage["elements"][number] {
  return { id, type: "photo", label, x, y, width, height, rotation: 0, zIndex, locked: false, fit: "cover" };
}

function caption(id: string, text: string, x: number, y: number, width: number, zIndex: number): LabPage["elements"][number] {
  return {
    id,
    type: "text",
    text,
    x,
    y,
    width,
    height: 1.2,
    rotation: 0,
    zIndex,
    locked: false,
    fontFamily: "Inter",
    fontSize: 14,
    fontWeight: "medium",
    color: "#1f2937",
    align: "center",
  };
}

function singlePrintPage(widthCm: number, heightCm: number): LabPage[] {
  return [
    {
      id: "pg_1",
      name: "Page 1",
      widthCm,
      heightCm,
      backgroundColor: "#ffffff",
      elements: [placeholderPhoto("el_photo", 0, 0, widthCm, heightCm, 1)],
    },
  ];
}

